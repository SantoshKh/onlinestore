/* add to cart handler */
$(document.body).on('click', '.add-to-cart', function() {
    var btn = $(this);
    var href = $(this).attr('data-href');
    var item = $(this).attr('data-item');
    $.ajax({
        url: href,
        type: "POST",
        data: {
            item: item
        },
        cache: false,
        success: function(returnText) {
            //replace the old content with the new content returned from the server.
            if (returnText === 'error') {
                alert("Submit Error!");
            } else {
                $('#shopping-cart-item-count').html(returnText);
                $(btn).html('Added');
                $(btn).prop('disabled', true);
            }
        },
        error: function(errorMessage) {
            alert("Submit Error: " + errorMessage);
        },
    });
    e.preventDefault();
});

/* end of add to cart handler */